int		ft_strcmp(char *s1, char *s2)
{
	int cur;

	cur = 0;
	while (s1[cur] != '\0' && s2[cur] != '\0' && s1[cur] == s2[cur])
	{
		cur++;
	}
	return (s1[cur] - s2[cur]);
}
