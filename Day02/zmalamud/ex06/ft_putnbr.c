void	ft_putnbr(int a)
{
	int array[20];
	int r;
	int i;

	i = 0;
	while (a >= 10)
	{
		r = a % 10;
		a = a / 10;
		array[i] = r + '0';
		i++;
	}
	array[i] = a + '0';
	while (i >= 0)
	{
		ft_putchar(array[i]);
		i--;
	}
	ft_putchar('\n');
}
